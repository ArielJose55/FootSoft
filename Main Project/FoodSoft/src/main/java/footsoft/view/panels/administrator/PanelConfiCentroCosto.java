    private void jButtonNewCentroCostoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonNewCentroCostoActionPerformed
        showOrHideFormulary(true);
        jPanelOperationNewUpdate.setVisible(false);
        centroDeCosto = new TipoDeEmpleado();
    }//GEN-LAST:event_jButtonNewCentroCostoActionPerformed

    private void jButtonTerminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonTerminarActionPerformed
         if(centroDeCosto == null) return;
         
         try{
             TipoDeEmpleadoDao tipoEmpleDao = new TipoDeEmpleadoDao();
             tipoEmpleDao.addTipoEmpleado(centroDeCosto);
             ((FSComboBoxModelGeneric)jList1.getModel()).addElement(centroDeCosto);
             showOrHideFormulary(false);
             showOHideButtons(false);
             jPanelWork.setVisible(false);
             jPanelOperationNewUpdate.setVisible(true);
             
         }catch(java.sql.SQLException ex){
             ex.printStackTrace();
         }
    }//GEN-LAST:event_jButtonTerminarActionPerformed


    private void changedPanelFoot(javax.swing.JPanel panel){
        jPanelWork.removeAll();
        jPanelWork.setLayout(new java.awt.BorderLayout());
        jPanelWork.add(panel, java.awt.BorderLayout.CENTER);
        jPanelWork.updateUI();
    }
    
    private void showOHideButtons(boolean show){
        jButtonRelacionarComida.setVisible(show);
        jButtonTerminar.setVisible(show);
    }
    
    private void showOrHideFormulary(boolean show){
        jLabel1.setVisible(show);
        jTextFieldCentroCostos.setVisible(show);
    }
    

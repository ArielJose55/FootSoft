    private void jButtonRelacionarComidaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonRelacionarComidaActionPerformed
        centroDeCosto.setNombre(jTextFieldCentroCostos.getText());
        jPanelWork.setVisible(true);
        jButtonRelacionarComida.setEnabled(false);
        PanelAddValorFoot panelAddValorFoot = new PanelAddValorFoot(centroDeCosto,jPanelWork,jButtonRelacionarComida);
        panelAddValorFoot.setBotonTerminar(jButtonTerminar);
        changedPanelFoot(panelAddValorFoot);
        
    }//GEN-LAST:event_jButtonRelacionarComidaActionPerformed

